DROP TABLE public.infection_type;

CREATE TABLE public.infection_type (
	"label" text NULL,
	value text NULL,
	count int4 NULL,
	infection_type text NULL
);

CREATE OR REPLACE VIEW public.positive_microbiology
AS SELECT DISTINCT m.subject_id,
    m.hadm_id,
    NULL::integer AS stay_id,
    1001 AS itemid,
    'positive_microbiology'::text AS linksto,
    m.charttime,
    'Positive Microbiology'::text AS category,
    m.value AS label,
    i.infection_type AS value
   FROM microbiologyresultsevents m
     JOIN infection_type i USING (label, value)
  WHERE m.valuenum = 1 AND m.hadm_id IS NOT NULL;


